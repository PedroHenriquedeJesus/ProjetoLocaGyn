    private void imprimirDadosNaGrid(ArrayList<Motorista> listaDeMotorista) {
        try {
            DefaultTableModel model = (DefaultTableModel) jTableMotorista.getModel();

            //Limpa a tabela 
            model.setNumRows(0);
            Iterator<Motorista> lista = listaDeMotorista.iterator();

            while (lista.hasNext()) {
                String[] saida = new String[5];
                Motorista aux = lista.next();
                //Endereco
                String EndeLogradouro = aux.getEndereco().getLogradouro() + " " + aux.getEndereco().getComplemento() + " "
                        + aux.getEndereco().getCep() + " " + aux.getEndereco().getBairro() + " " + aux.getEndereco().getCidade() + " "
                        + aux.getEndereco().getEstado();
                //Telefone 
                String TeleNumero = aux.getTelefone().getDdi() + " " + aux.getTelefone().getDdd() + " " + aux.getTelefone().getNumero();

                saida[0] = aux.getId()+ "";
                saida[1] = aux.getNome();
                saida[2] = aux.getNumeroCNH() + "";
                saida[3] = aux.getDataValidade().toString();
                saida[4] = EndeLogradouro;
                saida[5] = TeleNumero;

                //Incluir nova linha na Tabela
                Object[] dados = {saida[0], saida[1], saida[2], saida[3], saida[4], saida[5]};

                model.addRow(dados);
            }
        } catch (Exception erro) {

        }
    }
